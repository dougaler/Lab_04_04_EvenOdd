public class EvenOdd {
    public static void main(String[] args) {
        int numToExamine = 400;
        if((numToExamine % 2) == 0) {
            System.out.println("The number " + numToExamine + " is Even");
        }else{
            System.out.println("The number " + numToExamine + " is Odd");
        }


    }
}